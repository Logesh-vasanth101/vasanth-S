 class PositiveNegative {

    public static void main(String[] args) {

       int number = 3;
        System.out.println("Input:");
        
        if (number < 0)
            System.out.print(number + " \nOutput:\nNegative");

      
        else if ( number > 0)
            System.out.print(number + "\nOutput:\nPositive");

      
        else
            System.out.print(number + "\nOutput:\nZero");
    }
}
